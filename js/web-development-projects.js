document.addEventListener('DOMContentLoaded', () => {
    let output = "";
    const params = new URLSearchParams(window.location.search);
    const projectType = params.get('type');

    function getData() {
        for (let i = 0; i < projectData.length; i++) {
            if (projectData[i].type === projectType) {
                output += `
                <div class="col">
                    <div class="card shadow-sm" style="border: 2px solid #243642; padding:5px;">
                        <div class="card-body">
                            <h4 class="card-text" style="font-weight: bold;">${projectData[i].name}</h4>
                            <p class="card-text"> ${projectData[i].description}</p>
                            <p class="card-text" style="color:grey;">Languages: ${projectData[i].languages}</p>
                            <div class="d-flex justify-content-between align-items-center">
                                <div class="btn-group">
                                    <a href="${projectData[i].linkLive}">
                                        <button type="button" class="btn btn-sm btn-outline-secondary">View Site</button>
                                    </a>
                                    <a href="${projectData[i].linkCode}">
                                        <button type="button" class="btn btn-sm btn-outline-secondary">View Code</button>
                                    </a>
                                </div>
                            </div>
                        </div>
                    </div>
                </div>
                `;
            }
        }
        document.getElementById("projects-container").innerHTML = output;
    }

    getData(); // Call getData after DOM is ready
});


